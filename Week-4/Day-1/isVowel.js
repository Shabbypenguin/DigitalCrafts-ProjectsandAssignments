function isVowel(letter) {
	if (letter.toLowerCase() == "a" || letter.toLowerCase() == "e" || letter.toLowerCase() == "i" || letter.toLowerCase() == "o" || letter.toLowerCase() == "u") {
		console.log("It's a vowel!")
	}else {
			console.log("Not a vowel")
	}
}

isVowel("a")
isVowel("u")
isVowel("p")
isVowel("q")
isVowel("o")
isVowel("w")